// write a program to check weather the given value is digit or not?

class  Digit1
{
	public static void main(String[] args) 
	{
		char ch = '8';
		if (ch>='0'&&ch<='9')
		{
			System.out.println("given value is a digit");
		}
		else
		{
			System.out.println("given value is not a digit");
		}
	}
}
